package it.polimi.ingsw2.swim.exceptions;

/**
 * @author Administrator
 *
 */
public class LastAbilityDeletionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7560604986799175001L;

	/**
	 * 
	 */
	public LastAbilityDeletionException() {
		super();
	}

	/**
	 * @param message
	 */
	public LastAbilityDeletionException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public LastAbilityDeletionException(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public LastAbilityDeletionException(String message, Throwable cause) {
		super(message, cause);
	}

}
